#!/bin/bash
cd "$(dirname "$0")/.."
source ./script/setup.sh

zip_uri='' # mandatory
cask_name='' # mandatory
build_version="$(cat VERSION)"
while test $# -gt 0; do
    case $1 in
        --build-version) build_version="$2"; shift 2;;
        --zip-uri) zip_uri="$2"; shift 2;;
        --cask-name) cask_name="$2"; shift 2;;
        *) echo "Unknown arg $1"; exit 1;;
    esac
done

if test -z "$zip_uri"; then echo "--zip-uri is mandatory" > /dev/stderr; exit 1; fi
if test -z "$cask_name"; then echo "--cask-name is mandatory" > /dev/stderr; exit 1; fi

case "$cask_name" in
    airlock) conflicts_with_casks="conflicts_with cask: 'airlock-dev'";;
    airlock-dev) conflicts_with_casks="conflicts_with cask: 'airlock'";;
    *) echo "Unknown cask name: $cask_name. Allowed cask names: airlock, airlock-dev" > /dev/stderr; exit 1;;
esac

zip_file=''
if test -f "$zip_uri"; then
    zip_file=$zip_uri
    zip_uri="file://$(realpath "$zip_file")"
elif grep -q '^http' <<< "$zip_uri"; then
    zip_file=/tmp/Airlock-tmp.zip
    rm -rf $zip_file
    curl -L "$zip_uri" -o $zip_file
else
    echo "$zip_uri doesn't exist" > /dev/stderr; exit 1
fi
sha=$(shasum -a 256 "$zip_file" | awk '{print $1}')

cask_version=':latest' # Prevent 'Not upgrading airlock, the latest version is already installed'
zip_root_dir="Airlock-v$build_version"
if ! grep -q SNAPSHOT <<< "$build_version"; then
    cask_version="'$build_version'"
    zip_root_dir=$(sed "s/$build_version/#{version}/g" <<< "$zip_root_dir")
    zip_uri=$(sed "s/$build_version/#{version}/g" <<< "$zip_uri")
fi

rm -f ".release/$cask_name.rb" || true
mkdir -p .release
cat > ".release/$cask_name.rb" <<EOF
cask "$cask_name" do # THE FILE IS GENERATED BY build-brew-cask.sh
  version $cask_version
  sha256 "$sha"

  url "$zip_uri"
  name "Airlock"
  desc "Airlock is an i3-like tiling window manager for macOS"
  homepage "https://github.com/nikitabobko/Airlock"
  $conflicts_with_casks

  depends_on macos: ">= :ventura" # macOS 13

  postflight do
    system "xattr -d com.apple.quarantine #{staged_path}/$zip_root_dir/bin/airlock"
    system "xattr -d com.apple.quarantine #{appdir}/Airlock.app"
  end

  app "$zip_root_dir/Airlock.app"
  binary "$zip_root_dir/bin/airlock"

  binary "$zip_root_dir/shell-completion/zsh/_airlock",
      target: "#{HOMEBREW_PREFIX}/share/zsh/site-functions/_airlock"
  binary "$zip_root_dir/shell-completion/bash/airlock",
      target: "#{HOMEBREW_PREFIX}/etc/bash_completion.d/airlock"
  binary "$zip_root_dir/shell-completion/fish/airlock.fish",
      target: "#{HOMEBREW_PREFIX}/share/fish/vendor_completions.d/airlock.fish"

  Dir["#{staged_path}/$zip_root_dir/manpage/*"].each { |man| manpage man }
end
EOF
